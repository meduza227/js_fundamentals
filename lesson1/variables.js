// Declaring variables using var and let
var firstName = "John"
let lastName = "Doe"

console.log(firstName + " " + lastName)

// Declaring multiple variables in one line
var age, sex, dateOfBirth
age = 30
sex = "Male"
dateOfBirth = "1991-01-01"

console.log("Age: " + age + " Sex: " + sex + " Date of Birth: " + dateOfBirth)

// Using const for variables that won't change
const country = "USA"
console.log("Country: " + country)

